"""Run extracted design-patent pairs through the normal FastAPI job pipeline.

This script consumes manifests generated by ``scripts/build_design_dataset.py``.
It creates a temporary workbook with normal HTTP URLs:

    B = appno
    D = line drawing URL
    K = product photo URL

Then it exercises the same upload/job/start/SSE/rows flow as the browser.

Env:
    LINEBASE_DESIGN_DATASET=.data/design_dataset
    LINEBASE_DESIGN_SPLIT=test
    LINEBASE_DESIGN_LIMIT=10
    LINEBASE_DESIGN_MODEL=Qwen/Qwen3-VL-30B-A3B-Instruct
    LINEBASE_DESIGN_VERIFY=1
    LINEBASE_DESIGN_TILE_SCAN=0
    LINEBASE_DESIGN_PROMPT_VERSION=design_1
    LINEBASE_DESIGN_VERIFY_PROMPT_VERSION=design_1
"""
from __future__ import annotations

import asyncio
import contextlib
import json
import os
import socket
import subprocess
import sys
import time
from collections import Counter
from datetime import datetime
from pathlib import Path
from typing import Any

import httpx
import openpyxl

REPO = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(REPO / "src"))
sys.stdout.reconfigure(encoding="utf-8", errors="replace")  # type: ignore[union-attr]

SHEET = "design_eval"


def _env_flag(name: str, *, default: bool) -> bool:
    raw = os.environ.get(name, "").strip().lower()
    if not raw:
        return default
    if raw in {"1", "true", "yes", "on"}:
        return True
    if raw in {"0", "false", "no", "off"}:
        return False
    return default


def _pick_free_port() -> int:
    sock = socket.socket()
    sock.bind(("127.0.0.1", 0))
    port = int(sock.getsockname()[1])
    sock.close()
    return port


def _load_jsonl(path: Path) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    with path.open("r", encoding="utf-8") as fh:
        for line in fh:
            line = line.strip()
            if line:
                rows.append(json.loads(line))
    return rows


def _path_in_dataset(record_path: str, dataset_dir: Path) -> str:
    path = Path(record_path)
    if not path.is_absolute():
        path = REPO / path
    return path.resolve().relative_to(dataset_dir.resolve()).as_posix()


def _write_workbook(
    path: Path,
    *,
    records: list[dict[str, Any]],
    dataset_dir: Path,
    asset_port: int,
) -> list[dict[str, Any]]:
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = SHEET
    ws["A1"] = "dataset_id"
    ws["B1"] = "appno"
    ws["C1"] = "source_sheet"
    ws["D1"] = "logo_url"
    ws["E1"] = "product_url_source"
    ws["K1"] = "evidence_urls"

    url_records: list[dict[str, Any]] = []
    for offset, rec in enumerate(records, start=2):
        line_rel = _path_in_dataset(rec["line_path"], dataset_dir)
        product_rel = _path_in_dataset(rec["product_path"], dataset_dir)
        line_url = f"http://127.0.0.1:{asset_port}/{line_rel}"
        product_url = f"http://127.0.0.1:{asset_port}/{product_rel}"
        ws.cell(offset, 1, rec["id"])
        ws.cell(offset, 2, rec["appno"])
        ws.cell(offset, 3, rec["source_sheet"])
        ws.cell(offset, 4, line_url)
        ws.cell(offset, 5, rec.get("product_url") or "")
        ws.cell(offset, 11, product_url)
        url_records.append(
            {
                **rec,
                "workbook_row": offset,
                "line_url": line_url,
                "product_url": product_url,
            }
        )
    wb.save(path)
    return url_records


def _wait_for_health(port: int, timeout_s: float = 60.0) -> None:
    deadline = time.time() + timeout_s
    while time.time() < deadline:
        with contextlib.suppress(httpx.HTTPError):
            resp = httpx.get(f"http://127.0.0.1:{port}/api/dev/eval-runs", timeout=2.0)
            if resp.status_code < 500:
                return
        time.sleep(0.5)
    raise RuntimeError(f"uvicorn at :{port} did not become healthy")


def _wait_for_static(port: int, rel_path: str, timeout_s: float = 20.0) -> None:
    deadline = time.time() + timeout_s
    url = f"http://127.0.0.1:{port}/{rel_path}"
    while time.time() < deadline:
        with contextlib.suppress(httpx.HTTPError):
            resp = httpx.get(url, timeout=2.0)
            if resp.status_code == 200 and resp.content:
                return
        time.sleep(0.3)
    raise RuntimeError(f"asset server did not serve {url}")


async def _drain_events(client: httpx.AsyncClient, job_id: str) -> dict[str, Any]:
    last_job: dict[str, Any] = {}
    t0 = time.time()
    async with client.stream(
        "GET",
        f"/api/jobs/{job_id}/events",
        timeout=httpx.Timeout(1800.0, connect=10.0),
    ) as resp:
        resp.raise_for_status()
        buf = ""
        async for chunk in resp.aiter_text():
            buf += chunk.replace("\r\n", "\n")
            while "\n\n" in buf:
                raw, buf = buf.split("\n\n", 1)
                payload = "\n".join(
                    line.removeprefix("data:").strip()
                    for line in raw.splitlines()
                    if line.startswith("data:")
                )
                if not payload:
                    continue
                try:
                    ev = json.loads(payload)
                except json.JSONDecodeError:
                    continue
                dt = time.time() - t0
                et = ev.get("type")
                if et == "progress" and "job" in ev:
                    last_job = ev["job"]
                    print(
                        f"[{dt:6.1f}s] progress {last_job['status']} "
                        f"{last_job['done_rows']}/{last_job['total_rows']} "
                        f"cost=${last_job['cost_usd']:.4f}",
                        flush=True,
                    )
                elif et == "progress" and "row" in ev:
                    row = ev["row"]
                    print(
                        f"[{dt:6.1f}s] row.start id={row['id']} "
                        f"appno={row.get('appno')} status={row['status']}",
                        flush=True,
                    )
                elif et in {"row_done", "row_failed"}:
                    row = ev["row"]
                    print(
                        f"[{dt:6.1f}s] {et} appno={row.get('appno')} "
                        f"status={row['status']} conf={row.get('best_confidence')} "
                        f"crop={row.get('best_crop_path')}",
                        flush=True,
                    )
                elif et == "warning":
                    print(f"[{dt:6.1f}s] warning {ev.get('message')}", flush=True)
                elif et == "finished":
                    last_job = ev["job"]
                    print(
                        f"[{dt:6.1f}s] finished {last_job['status']} "
                        f"{last_job['done_rows']}/{last_job['total_rows']} "
                        f"cost=${last_job['cost_usd']:.4f}",
                        flush=True,
                    )
                    return last_job
    return last_job


def _meta_for_row(row: dict[str, Any]) -> dict[str, Any]:
    meta = row.get("match_meta") or {}
    if not isinstance(meta, dict):
        return {}
    best_url = row.get("best_evidence_url")
    if best_url and isinstance(meta.get(best_url), dict):
        return meta[best_url]
    for value in meta.values():
        if isinstance(value, dict):
            return value
    return {}


def _summarize_rows(rows: list[dict[str, Any]]) -> dict[str, Any]:
    statuses = Counter(str(row.get("status")) for row in rows)
    found = 0
    verified_true = 0
    bbox_count = 0
    crop_count = 0
    errors: Counter[str] = Counter()
    verify_fits: Counter[str] = Counter()
    confidences: list[float] = []

    for row in rows:
        meta = _meta_for_row(row)
        if meta.get("found"):
            found += 1
        if meta.get("verified") is True:
            verified_true += 1
        if meta.get("bbox"):
            bbox_count += 1
        if row.get("best_crop_path"):
            crop_count += 1
        if meta.get("fit"):
            verify_fits[str(meta.get("fit"))] += 1
        if meta.get("error"):
            errors[str(meta.get("error"))[:120]] += 1
        conf = row.get("best_confidence")
        if conf is not None:
            with contextlib.suppress(TypeError, ValueError):
                confidences.append(float(conf))

    return {
        "total_rows": len(rows),
        "statuses": dict(statuses),
        "found_rows": found,
        "verified_true_rows": verified_true,
        "bbox_rows": bbox_count,
        "crop_rows": crop_count,
        "verify_fits": dict(verify_fits),
        "errors": dict(errors),
        "mean_best_confidence": (
            sum(confidences) / len(confidences) if confidences else None
        ),
    }


async def _run() -> int:
    dataset_dir = Path(os.environ.get("LINEBASE_DESIGN_DATASET", ".data/design_dataset"))
    if not dataset_dir.is_absolute():
        dataset_dir = REPO / dataset_dir
    split = os.environ.get("LINEBASE_DESIGN_SPLIT", "test").strip() or "test"
    limit = int(os.environ.get("LINEBASE_DESIGN_LIMIT", "10"))
    model = os.environ.get(
        "LINEBASE_DESIGN_MODEL",
        "Qwen/Qwen3-VL-30B-A3B-Instruct",
    ).strip()
    verify = _env_flag("LINEBASE_DESIGN_VERIFY", default=True)
    tile_scan = _env_flag("LINEBASE_DESIGN_TILE_SCAN", default=False)
    prompt_version = os.environ.get("LINEBASE_DESIGN_PROMPT_VERSION", "design_1").strip()
    verify_prompt_version = os.environ.get(
        "LINEBASE_DESIGN_VERIFY_PROMPT_VERSION",
        "design_1",
    ).strip()

    manifest = dataset_dir / f"{split}.jsonl"
    if not manifest.exists():
        raise FileNotFoundError(
            f"{manifest} missing; run scripts/build_design_dataset.py first"
        )
    records = _load_jsonl(manifest)
    if limit > 0:
        records = records[:limit]
    if not records:
        raise RuntimeError(f"No records selected from {manifest}")

    run_id = datetime.now().strftime("%Y%m%d_%H%M%S")
    out_dir = REPO / ".data" / "design_eval" / f"{run_id}_{split}_{len(records)}"
    out_dir.mkdir(parents=True, exist_ok=True)

    asset_port = _pick_free_port()
    api_port = _pick_free_port()
    workbook = out_dir / "design_eval.xlsx"
    url_records = _write_workbook(
        workbook,
        records=records,
        dataset_dir=dataset_dir,
        asset_port=asset_port,
    )

    asset_log = (out_dir / "asset_http.log").open("wb")
    api_log = (out_dir / "uvicorn.log").open("wb")
    env = dict(os.environ)
    env["PYTHONPATH"] = str(REPO / "src")
    env["PYTHONUNBUFFERED"] = "1"
    if prompt_version:
        env["LINEBASE_PROMPT_VERSION"] = prompt_version
    if verify_prompt_version:
        env["LINEBASE_VERIFY_PROMPT_VERSION"] = verify_prompt_version

    print(f"[design-e2e] dataset={dataset_dir.relative_to(REPO)} split={split} rows={len(records)}")
    print(f"[design-e2e] model={model} verify={verify} tile_scan={tile_scan}")
    if prompt_version:
        print(f"[design-e2e] prompt_version={prompt_version}")
    if verify_prompt_version:
        print(f"[design-e2e] verify_prompt_version={verify_prompt_version}")
    print(f"[design-e2e] out={out_dir.relative_to(REPO)}")

    asset_proc = subprocess.Popen(
        [sys.executable, "-u", "-m", "http.server", str(asset_port), "--bind", "127.0.0.1"],
        cwd=str(dataset_dir),
        stdout=asset_log,
        stderr=subprocess.STDOUT,
    )
    api_proc = subprocess.Popen(
        [
            sys.executable,
            "-u",
            "-m",
            "uvicorn",
            "linebase.server:app",
            "--host",
            "127.0.0.1",
            "--port",
            str(api_port),
            "--log-level",
            "warning",
        ],
        cwd=str(REPO),
        env=env,
        stdout=api_log,
        stderr=subprocess.STDOUT,
    )

    try:
        _wait_for_static(asset_port, _path_in_dataset(records[0]["line_path"], dataset_dir))
        _wait_for_static(asset_port, _path_in_dataset(records[0]["product_path"], dataset_dir))
        _wait_for_health(api_port)

        async with httpx.AsyncClient(
            base_url=f"http://127.0.0.1:{api_port}",
            timeout=httpx.Timeout(900.0, connect=10.0),
        ) as client:
            with workbook.open("rb") as fh:
                files = {
                    "file": (
                        workbook.name,
                        fh,
                        "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
                    ),
                }
                upload_resp = await client.post("/api/uploads", files=files, timeout=120.0)
            upload_resp.raise_for_status()
            upload = upload_resp.json()

            row_ids = [rec["workbook_row"] for rec in url_records]
            job_req = {
                "upload_id": upload["id"],
                "sheet_name": SHEET,
                "appno_column": "B",
                "logo_column": "D",
                "evidence_column": "K",
                "sample_kind": "row_ids",
                "sample_params": {"ids": row_ids},
                "threshold": 0.5,
                "model": model,
                "verify_loop": verify,
                "tile_scan": tile_scan,
            }
            job_resp = await client.post("/api/jobs", json=job_req)
            job_resp.raise_for_status()
            job = job_resp.json()
            job_id = job["id"]
            print(f"[design-e2e] job={job_id} total_rows={job['total_rows']}")

            start_resp = await client.post(f"/api/jobs/{job_id}/start")
            start_resp.raise_for_status()
            final_job = await _drain_events(client, job_id)

            rows_resp = await client.get(f"/api/jobs/{job_id}/rows")
            rows_resp.raise_for_status()
            rows = rows_resp.json()

    finally:
        for proc in (api_proc, asset_proc):
            proc.terminate()
        for proc in (api_proc, asset_proc):
            with contextlib.suppress(subprocess.TimeoutExpired):
                proc.wait(timeout=5)
            if proc.poll() is None:
                proc.kill()
        asset_log.close()
        api_log.close()

    by_row = {rec["workbook_row"]: rec for rec in url_records}
    enriched_rows: list[dict[str, Any]] = []
    for row in rows:
        rec = by_row.get(int(row["row_index"]))
        enriched_rows.append({"dataset_record": rec, "pipeline_row": row})

    summary = _summarize_rows(rows)
    result = {
        "job": final_job,
        "job_id": job_id,
        "dataset_dir": dataset_dir.relative_to(REPO).as_posix(),
        "split": split,
        "limit": limit,
        "model": model,
        "verify_loop": verify,
        "tile_scan": tile_scan,
        "prompt_version": prompt_version or None,
        "verify_prompt_version": verify_prompt_version or None,
        "workbook": workbook.relative_to(REPO).as_posix(),
        "summary": summary,
    }

    (out_dir / "rows.json").write_text(
        json.dumps(enriched_rows, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    (out_dir / "result.json").write_text(
        json.dumps(result, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )

    print("[design-e2e] summary")
    print(json.dumps(summary, ensure_ascii=False, indent=2))
    for row in rows:
        meta = _meta_for_row(row)
        print(
            f"  row={row['row_index']} appno={row.get('appno')} status={row.get('status')} "
            f"found={meta.get('found')} verified={meta.get('verified')} "
            f"fit={meta.get('fit')} conf={row.get('best_confidence')} "
            f"bbox={meta.get('bbox')}",
        )
    print(f"[design-e2e] wrote {out_dir.relative_to(REPO)}")
    return 0


def main() -> int:
    return asyncio.run(_run())


if __name__ == "__main__":
    raise SystemExit(main())
